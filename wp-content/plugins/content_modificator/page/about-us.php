            <h2 class="subtitle" style="background-image: url('<?php echo get_carousel_image_url(); ?>');">NOTRE MISSION</h2>
            <div class="p">

            <p>Promouvoir les Femmes Malagaches et les Inspirer à devenir Moteurs de Développement
                Le MOTEUR <br> est un générateur de mouvement, qui entraîne un changement, une évolution... Pour les femmes, il ne s'agit plus seulement d'être de bonnes mères et épouses, mais aussi d'être une force motrice d'évolution, inspirant conjoints, enfants et communauté à lutter contre les maux sociaux, à faire le bien et à motiver les autres à avoir un impact positif, où qu'ils soient.</p>

            <p> Le MOTEUR a de la puissance ; plus il est fort, plus son action est significative, et plus il réalise de travail. Notre première transformation vise à renforcer les femmes.</p>

            <p> Pour accomplir cette mission, la Fondation se concentrera sur l'éducation, l'entrepreneuriat, la santé, et le social, visant à permettre aux femmes de s'épanouir en tant que citoyennes et individus, contribuant ainsi au développement de la nation. Étant donné que le taux de pauvreté en Madagascar est très élevé, atteignant 77,4 % de la population, le développement des femmes, qui sont majoritaires, contribuera efficacement à réduire ce taux.</p>
