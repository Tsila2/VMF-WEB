            <h2 class="subtitle" style="background-image: url('<?php echo get_carousel_image_url(); ?>');">NOTRE HISTOIRE</h2>
            <div class="p">

            <p>En dépit des ravages causés par la pandémie de Covid-19 dans le monde, c'est pendant le confinement de 2020 que Dieu a inspiré Pasteur Annick P. RAKOTOARISOA à fonder une organisation en faveur des femmes malgaches. Ces femmes étaient déjà confrontées à de nombreuses difficultés économiques, sociales et de santé, bien avant l'avènement de la Covid-19.</p>

            <p>Cette vision a guidé la création de la Fondation, définissant sa mission et ses valeurs. Les domaines d'intervention et les objectifs ont évolué, permettant ainsi à la Fondation de se formaliser.</p>

            <p>La vision partagée a suscité l'intérêt d'autres femmes et jeunes femmes, qui ont rejoint la Fondation "Vehivavy Môteran'ny Fampandroasoana" ou "Femmes Moteurs de Développement". Ceci s'est manifesté par la création d'un groupe Facebook le 23 juillet 2020. L'objectif de ce groupe était de sensibiliser les femmes malgaches au développement personnel ainsi qu'à la contribution au développement de leurs communautés et de leur pays. Depuis sa création, ce groupe a attiré de plus en plus de membres, comptant aujourd'hui plus de 1 700 adhérents.</p>

            <p>Par la suite, des actions tangibles ont été entreprises, comme la distribution de vêtements dans le quartier d'Andavamamba à Antananarivo. Cette première action de la Fondation a été diffusée sur RTA news (Radio Télévision Analamanga). D'autres initiatives ont suivi, telles que le reboisement, le don de sang, les visites en prison, et bien d'autres.</p>

            <p>L'Association a été officiellement enregistrée le 29 septembre 2022 sur le territoire de la République de Madagascar.</p>


            <p>Nous exprimons notre gratitude envers tous les membres et sympathisants pour leurs diverses contributions, ayant permis à la Fondation de devenir un véritable catalyseur pour les femmes malgaches, les aidant à devenir des actrices du développement de leurs communautés et de leur magnifique pays, Madagascar.</p>


            <p> Comme dit dans Philippiens 1:6, "Celui qui a commencé en vous cette bonne œuvre la rendra parfaite pour le jour de Jésus Christ".</p>
