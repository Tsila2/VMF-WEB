        <h2 class="subtitle2" style="background-image: url('<?php echo esc_url(home_url('/wp-content/uploads/2023/09/fond-colore-de-roses-24864596.jpg')); ?>');">NOTRE VISION</h2>
            <div class="p">

            <p>Le terme malgache pour "nation", "Firenena", trouve son origine dans "reny", signifiant "mères". Ainsi, les mères ont une place centrale dans la nation malgache. Chaque foyer malgache peut être comparé à une cellule essentielle du corps de la nation. Si une grande partie des foyers malgaches sont équilibrés, le pays sera en meilleure santé. Les mères, jouant un rôle central, portent donc une immense responsabilité et méritent une attention particulière. C'est la raison d'être de la vision de VMF : Femmes Moteurs de Développement.</p>

            <p>La Fondation "Femmes Moteurs de Développement" incarne un mouvement de femmes générant l'évolution et le développement, tant individuel que national. Ensemble, nous réaliserons cette vision, comme le proverbe malgache le dit : "IZAY MITAMBATRA VATO, IZAY MISARAKA FASIKA" - "Ceux qui sont unis sont des pierres, ceux qui sont séparés sont des sables", signifiant que l'union fait la force. En nous unissant, nous nous encourageons mutuellement et formons une force collective pour surmonter la pauvreté, l'analphabétisme, la discrimination, l'inégalité, le harcèlement et d'autres défis.</p>

            <p>Comme dit un proverbe arabe de 1855 : "Quand vous éduquez les femmes, vous construisez la société ; quand vous élevez un homme, vous n'en élevez qu'un seul". Ceci justifie le choix de VMF de prioriser les femmes pour reconstruire la société, d'autant plus qu'elles représentent 50,11% de la population malgache (source ONU 2020).</p>
